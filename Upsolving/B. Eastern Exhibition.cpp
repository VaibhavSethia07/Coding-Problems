void solve(vector<long>& x,vector<long>& y,int N){
	
	sort(all(x));
	sort(all(y));
	
	if(N&1)
		cout<<1<<"\n";
	else
		cout<<(x[(N+2)/2-1]-x[(N+1)/2-1]+1)*1ll*(y[(N+2)/2-1]-y[(N+1)/2-1]+1)<<"\n";
}

